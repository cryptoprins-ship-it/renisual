let currentLocale: Locale = DEFAULT_LOCALE;
const subscribers = new Set<() => void>();

export function getLocale(): Locale {
  return currentLocale;
}

export function setGlobalLocale(loc: Locale) {
  if (!SUPPORTED_LOCALES.includes(loc)) return;
  currentLocale = loc;
  if (typeof window !== "undefined") {
    try {
      localStorage.setItem(STORAGE_KEY, loc);
    } catch {}
    syncHtmlLang(loc);
  }
  subscribers.forEach((fn) => fn());
}

function fmt(template: string, params?: Record<string, string | number>): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, k) => {
    const v = params[k];
    return v === undefined || v === null ? "" : String(v);
  });
}

const warnedMissingKeys = new Set<string>();

export function translate(locale: Locale, key: string, params?: Record<string, string | number>): string {
  // Fallback chain: requested locale → English → Dutch → key itself.
  const direct = TRANSLATIONS[locale]?.[key];
  const value = direct ?? TRANSLATIONS.en[key] ?? TRANSLATIONS.nl[key] ?? key;

  // Final-line defence: if the resolver still returned the raw key, prefer a
  // related fallback over leaking dotted strings into the UI. Today only
  // gc.side.* uses dots; extend the prefix list when more namespaces appear.
  if (value === key && key.includes(".")) {
    const generic =
      TRANSLATIONS[locale]?.["gc.side.numbered"] ??
      TRANSLATIONS.en["gc.side.numbered"] ??
      TRANSLATIONS.nl["gc.side.numbered"];
    if (key.startsWith("gc.side.") && generic) {
      return fmt(generic, { n: params?.n ?? "" });
    }
  }

  if (
    process.env.NODE_ENV !== "production" &&
    direct === undefined &&
    typeof window !== "undefined"
  ) {
    const id = `${locale}::${key}`;
    if (!warnedMissingKeys.has(id)) {
      warnedMissingKeys.add(id);
      const used =
        TRANSLATIONS.en[key] !== undefined
          ? "en"
          : TRANSLATIONS.nl[key] !== undefined
          ? "nl"
          : "key";
      // eslint-disable-next-line no-console
      console.warn(`[i18n] missing key "${key}" for locale "${locale}" — using ${used} fallback`);
    }
  }

  return fmt(value, params);
}

let ipDetectionFired = false;

async function detectLocaleFromIp(): Promise<Locale | null> {
  try {
    const res = await fetch("/api/detect-locale", { cache: "default" });
    if (!res.ok) return null;
    const data = (await res.json()) as { locale?: string };
    if (data?.locale && (SUPPORTED_LOCALES as string[]).includes(data.locale)) {
      return data.locale as Locale;
    }
  } catch {
    /* silent — fall back to default locale */
  }
  return null;
}

function syncHtmlLang(loc: Locale) {
  if (typeof document !== "undefined") {
    document.documentElement.lang = loc;
  }
}

export function useLocale() {
  const [locale, setLocaleLocal] = useState<Locale>(currentLocale);

  useEffect(() => {
    if (typeof window === "undefined") return;

    // Manual preference (set by clicking the language switcher) wins over IP.
    let savedManual: Locale | null = null;
    try {
      const saved = localStorage.getItem(STORAGE_KEY) as Locale | null;
      if (saved && SUPPORTED_LOCALES.includes(saved)) savedManual = saved;
    } catch {}

    if (savedManual) {
      if (savedManual !== currentLocale) {
        currentLocale = savedManual;
        setLocaleLocal(savedManual);
        subscribers.forEach((fn) => fn());
      }
      syncHtmlLang(savedManual);
    } else if (!ipDetectionFired) {
      ipDetectionFired = true;
      // First visit, no manual choice yet — infer from IP. We do NOT persist
      // the result so a future visit re-detects (cheap, browser-cached for a day)
      // and so a manual pick remains the only thing in localStorage.
      detectLocaleFromIp().then((loc) => {
        const next = loc ?? currentLocale;
        if (next !== currentLocale) {
          currentLocale = next;
          subscribers.forEach((fn) => fn());
        }
        syncHtmlLang(next);
      });
    } else {
      syncHtmlLang(currentLocale);
    }

    const update = () => setLocaleLocal(currentLocale);
    subscribers.add(update);
    return () => {
      subscribers.delete(update);
    };
  }, []);

  const setLocale = useCallback((loc: Locale) => setGlobalLocale(loc), []);

  const t = useCallback(
    (key: string, params?: Record<string, string | number>) => translate(locale, key, params),
    [locale]
  );

  return { locale, setLocale, t };
}
